func check1(string1: String, string2: String) -> Bool{
    var checkString = string2
    for letter in string1{
        if let index = checkString.firstIndex(of: letter){
            checkString.remove(at: index)}
        else{
            return false
        }
    }
    return checkString.count == 0
}

